#include <iostream>

using namespace std;

int main()
{
    //Here are some variables and their types

    int integer = 1;            //simple whole numbers
    float Float = 3.14;         //numbers with decimals
    double Double = 33.33333;   //larger numbers with decimals

    string name = "Jabari";       //words and letters
    char character = 'A';       //single letters and symbols

    cout << "This is an integer: " << integer << endl;
    cout << "This is an float: " << Float << endl;
    cout << "This is an double: " << Double << endl;
    cout << "This is an string: " << name << endl;
    cout << "This is an character: " << character << endl;

    return 0;                   //REMEBER!: always follow the return types
                                //          of functions
}
